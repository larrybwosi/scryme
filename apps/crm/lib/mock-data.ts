export function getCustomerById(id: string): Customer | undefined {
  return customers.find((c) => c.id === id);
}

export function getCustomerStats() {
  const total = customers.length;
  const active = customers.filter((c) => c.status === 'Active' || c.status === 'VIP').length;
  const vip = customers.filter((c) => c.status === 'VIP').length;
  const leads = customers.filter((c) => c.status === 'Lead').length;
  const inactive = customers.filter((c) => c.status === 'Inactive').length;
  const totalRevenue = customers.reduce((sum, c) => sum + c.totalRevenue, 0);
  const avgOrderValue = totalRevenue / customers.reduce((sum, c) => sum + c.totalOrders, 0);
  const openInvoices = customers.reduce((sum, c) => sum + c.openInvoices, 0);

  return { total, active, vip, leads, inactive, totalRevenue, avgOrderValue, openInvoices };
}

export function formatCurrency(amount: number, currency = 'USD'): string {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency,
    minimumFractionDigits: 0,
    maximumFractionDigits: 0,
  }).format(amount);
}

export function formatDate(dateStr: string): string {
  return new Date(dateStr).toLocaleDateString('en-US', {
    month: 'short',
    day: 'numeric',
    year: 'numeric',
  });
}

export function formatDateTime(dateStr: string): string {
  return new Date(dateStr).toLocaleString('en-US', {
    month: 'short',
    day: 'numeric',
    year: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
  });
}
