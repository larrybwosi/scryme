export type CustomerStatus = 'Active' | 'Inactive' | 'VIP' | 'Lead';
export type CustomerType = 'B2C' | 'B2B' | 'Enterprise';
export type InvoiceStatus = 'Draft' | 'Sent' | 'Paid' | 'Overdue' | 'Void';
export type DeliveryStatus = 'Pending' | 'In Transit' | 'Delivered' | 'Cancelled' | 'Failed';
export type OrderStatus = 'Pending' | 'Processing' | 'Completed' | 'Cancelled' | 'Refunded';
export type OrderChannel = 'POS' | 'Online' | 'B2B' | 'Phone';
export type ConversationChannel = 'Phone' | 'Email' | 'In-Person' | 'Chat' | 'Video Call';
export type ConversationDirection = 'Inbound' | 'Outbound';
export type FollowUpPriority = 'High' | 'Medium' | 'Low';
export type FollowUpStatus = 'Pending' | 'Completed' | 'Overdue' | 'Cancelled';

export interface Note {
  id: string;
  customerId: string;
  content: string;
  author: string;
  authorInitials: string;
  createdAt: string;
  pinned?: boolean;
}

export interface Delivery {
  id: string;
  customerId: string;
  orderRef: string;
  items: string;
  address: string;
  status: DeliveryStatus;
  driver: string;
  scheduledDate: string;
  deliveredDate?: string;
  trackingNumber: string;
}

export interface Invoice {
  id: string;
  customerId: string;
  invoiceNumber: string;
  date: string;
  dueDate: string;
  items: string;
  subtotal: number;
  tax: number;
  total: number;
  amountPaid: number;
  status: InvoiceStatus;
}

export interface Order {
  id: string;
  customerId: string;
  orderNumber: string;
  type: OrderChannel;
  date: string;
  itemCount: number;
  items: string;
  subtotal: number;
  discount: number;
  tax: number;
  total: number;
  status: OrderStatus;
  channel: string;
}

export interface Conversation {
  id: string;
  customerId: string;
  channel: ConversationChannel;
  direction: ConversationDirection;
  date: string;
  subject: string;
  summary: string;
  loggedBy: string;
  loggedByInitials: string;
  duration?: string;
}

export interface FollowUp {
  id: string;
  customerId: string;
  title: string;
  description: string;
  dueDate: string;
  priority: FollowUpPriority;
  status: FollowUpStatus;
  assignedTo: string;
  assignedToInitials: string;
  completedDate?: string;
}

export interface Customer {
  id: string;
  name: string;
  email: string;
  phone: string;
  company: string;
  type: CustomerType;
  status: CustomerStatus;
  tags: string[];
  loyaltyPoints: number;
  address: string;
  city: string;
  country: string;
  avatar?: string;
  website?: string;
  industry: string;
  accountManager: string;
  accountManagerInitials: string;
  createdAt: string;
  lastActivity: string;
  totalRevenue: number;
  totalOrders: number;
  openInvoices: number;
  healthScore: number; // 0–100
  notes: Note[];
  deliveries: Delivery[];
  invoices: Invoice[];
  orders: Order[];
  conversations: Conversation[];
  followUps: FollowUp[];
}

